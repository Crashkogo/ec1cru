async function generateTestimonials() {
  console.log('Генерация тестовых отзывов...');

  try {
    for (let i = 0; i < testimonials.length; i++) {
      const testimonial = testimonials[i];
      const slug = slugify(testimonial.company, {
        lower: true,
        strict: true,
        locale: 'ru'
      });

      await prisma.testimonial.create({
        data: {
          companyName: testimonial.company,
          content: testimonial.content,
          slug: slug,
          isPublished: true,
        },
      });

      console.log(`✓ Создан отзыв от ${testimonial.company}`);
    }

    console.log('\nУспешно создано 5 тестовых отзывов!');
  } catch (error) {
    console.error('Ошибка при генерации отзывов:', error);
  } finally {
    await prisma.$disconnect();
  }
}

generateTestimonials();
